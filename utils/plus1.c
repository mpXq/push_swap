int	*plus1(int *a)
{
	int	i;
	int	len;
	int	*a2;

	i = -1;
	len = ft_intplen(a);
	a2 = malloc(sizeof(int) * (len + 1));
	while (++i <= len)
		a2[i + 1] = a[i];
	a2[0] = 0;
	return (a2);
}
