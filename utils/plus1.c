#include "utils.h"

int	*plus1(int *a)
{
	int	i;
	int	tmp;
	int	len;

	i = 0;
	len = ft_intplen(a);
	tmp = 0;
	while (i < len)
	{
		tmp = a[i + 1];
		a[i + 1] = a[i];
		i++;
	}
	return (a);
}
