#include "utils.h"

int	*minus1(int *a)
{
	int	i;
	int	tmp;
	int	len;

	i = ft_intplen(a);
	tmp = 0;
	while (i - 1)
	{
		if (i > 0)
		{
			tmp = a[i - 1];
			a[i - 1] = a[i];
			i--;
		}
	}
	return (a);
}
