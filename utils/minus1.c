#include "utils.h"

int	*minus1(int *a)
{
	int	i;
	int	*a2;

	i = ft_intplen(a);
	a2 = malloc (sizeof(int) * i - 1);
	if (!a2)
		return (0);
	while (--i)
		a2[i] = a[i + 1];
	free(a);
	return (a2);
}
